import colorgram

rgb_colors = []
colors = colorgram.extract('Day18\hirst-painting-start\image.jpg', 30)
for color in colors:
    r = color.rgb.r
    g = color.rgb.g
    b = color.rgb.b
    new_color = (r, g, b)
    rgb_colors.append(new_color)

# Draw the dots
import turtle
import random

turtle.colormode(255)
tim = turtle.Turtle()
tim.shape("turtle")
color_list = rgb_colors
tim.speed("fastest")

tim.hideturtle()
tim.penup()
tim.setheading(225)
tim.forward(350)
tim.setheading(0)
number_of_dots = 100

for dot_count in range(1, number_of_dots + 1):
    tim.dot(20, random.choice(color_list))
    tim.penup()
    tim.forward(50)
    
    if dot_count % 10 == 0:
        tim.setheading(90)
        tim.forward(50)
        tim.setheading(180)
        tim.forward(500)
        tim.setheading(0)

screen = turtle.Screen()
screen.exitonclick()





